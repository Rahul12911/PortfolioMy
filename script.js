    AOS.init({
        duration: 1000,
        easing: 'ease-in-out',
        once: true,
        mirror: false
    });

    // Initialize all components
    initNavigation();
    initTypingEffect();
    initScrollEffects();
    initSkillBars();
    initCounters();
    initContactForm();
    initBackToTop();
    initMobileMenu();
